export function t(locale: Locale, key: string, fallback?: string): any {
  const keys = key.split(".");
  let value: any = translations[locale];

  for (const k of keys) {
    value = value?.[k];
  }

  if (value !== undefined) {
    return value;
  }

  // Fallback to English
  if (locale !== "en") {
    let englishValue: any = translations.en;
    for (const k of keys) {
      englishValue = englishValue?.[k];
    }
    if (englishValue !== undefined) {
      return englishValue;
    }
  }

  return fallback || key;
}

