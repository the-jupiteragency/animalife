export function getProductName(category: string, locale: Locale): string {
  const categoryMap: Record<string, string> = {
    "Small Breed Adults": t(locale, "products.categories.small"),
    "Medium & Large Breed Adults": t(locale, "products.categories.medium"),
    "Giant Breed Adults": t(locale, "products.categories.giant"),
  };
  return categoryMap[category] || category;
}

